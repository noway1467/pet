def neglect_line(address):
    return _fmt(random.choice(NEGLECT), address)


def greet_line(key, address, level_index=0):
    groups = GREET_BY_LEVEL.get(level_index) or GREET_BY_LEVEL[0]
    pool = groups.get(key) or groups["morning"]
    return _fmt(random.choice(pool), address)


def _pick(pool, address, avoid=None):
    cands = [s for s in pool if s != avoid] or pool
    return _fmt(random.choice(cands), address)
